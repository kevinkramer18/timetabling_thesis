class GeneralCourse:

    def __init__(self, course_code, section, faculty_name, room, start_year, end_year, term, start_time, end_time, day1, day2):
        self.course_code = course_code
        self.section = section
        self.faculty_name = faculty_name
        self.room = room
        self.start_year = start_year
        self.end_year = end_year
        self.term = term
        self.start_time = start_time
        self.end_time = end_time
        self.day1 = day1
        self.day2 = day2

