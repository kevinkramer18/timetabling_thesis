class Professor:

    def _init_(self, faculty_id, last_name, first_name, units, load, preferred_courses, preferred_timeslots):
        self.faculty_id = faculty_id
        self.last_name = last_name
        self.first_name = first_name
        self.units = units
        self.load = load
        self.preferred_courses = preferred_courses
        self.preferred_timeslots = preferred_timeslots

